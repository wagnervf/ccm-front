<template>
  <div>
    <v-card class="mx-1 mb-1">
      <v-card-title class="pa-6 pb-0">
        <v-row no-gutters>
          <v-col cols="12" class="d-flex align-center">
            <p>Quantitativo de Simulações Estados</p>
          </v-col>
        </v-row>
      </v-card-title>
      <v-card-text class="pa-6">
        <v-row>
          <v-col>
            <ApexChart
              type="bar"
              height="350"
              :options="chartOptions"
              :series="series"
            ></ApexChart>
          </v-col>
        </v-row>
      </v-card-text>
    </v-card>
  </div>
</template>

<script>
import eCow from "./e-cow";
import ApexChart from "vue-apexcharts";

export default {
  name: "Dashboard-Chart-Column",
  components: {
    ApexChart,
  },
  data() {
    return {
      eCow,
      series: [
        {
          data: [],
          //[400, 430, 448, 470, 540, 580, 690, 1100, 1200, 1380],
        },
      ],
      chartOptions: {
        chart: {
          type: "bar",
          height: 350,
        },       
        dataLabels: {
          enabled: true,
          textAnchor: "start",
          style: {
            colors: ["#fff"],
          },
        },
        plotOptions: {
          bar: {
            borderRadius: 4,
            horizontal: true,
          },
        },

        xaxis: {
          categories: [],
          /*[
            "South Korea",
            "Canada",
            "United Kingdom",
            "Netherlands",
            "Italy",
            "France",
            "Japan",
            "United States",
            "China",
            "Germany",
          ],*/
        },
      },

      estados: [],
      estadosSeparados: [],
    };
  },

  mounted() {
    this.getEstados();
    this.separaRacasTouros();
    console.log(this.series["0"]);
  },

  computed: {},

  methods: {
    getEstados() {
      var data = this.eCow;
      Object.values(data).forEach((value) => {
        this.estados.push(value.state);
      });
    },

    separaRacasTouros() {
      const counts = {};
      //this.estados.sort();

      

       


      this.estados.forEach(function (x) {
        counts[x] = (counts[x] || 0) + 1;
      });
      Object.assign(this.estadosSeparados, counts);

      console.log(this.estadosSeparados)

      /* ****  Corrigir ordenação ****/

     this.estadosSeparados =  this.estadosSeparados.sort(function (a, b) {
          if (a.value > b.value) {
            return 1;
          }
          if (a.value < b.value) {
            return -1;
          }
          // a must be equal to b
          return 0;
        });


        console.log(this.estadosSeparados)

      Object.assign(
        this.chartOptions.xaxis.categories,
        Object.keys(this.estadosSeparados)
      );
      this.series["0"].data = Object.values(this.estadosSeparados);

      
    },
  },
};
</script>

